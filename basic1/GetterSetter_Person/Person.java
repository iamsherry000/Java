package basic1.GetterSetter_Person;

public class Person {
    private String name;
    private int age;
    
    Person() { // constructor
        this.name = name;
        this.age = age;
    }

    String getName() {
        return name;
    }

    int getAge() {
        return age;
    }

    void setName(String name) {
        this.name = name;
    }

    void setAge(int age) {
        this.age = age;
    }

}
